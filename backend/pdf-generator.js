/**
 * pdf-generator.js
 * ────────────────
 * Builds a structured, visually appealing engineering-drawing PDF report
 * using PDFKit.  The layout is:
 *
 *   Cover header  →  Title block table  →  Part summary
 *   →  OCR highlights  →  Understanding
 *   →  SUBPARTS (each with its own dimension table)
 *   →  FULL ASSEMBLY DIMENSIONS table
 *   →  Interpreted features table
 *   →  Review & notes
 *   →  Document comparison (if multi-file)
 *
 * Every table is bounded inside the safe margin area and word-wraps
 * so nothing bleeds off the page.
 */

const PDFDocument = require("pdfkit");

/* ── colour palette ─────────────────────────────────────────────── */
const C = {
  bg:           "#FFFFFF",
  headerBg:     "#0F2B46",
  headerFg:     "#FFFFFF",
  accent:       "#1A73E8",
  accentLight:  "#E8F0FE",
  sectionBg:    "#F1F3F5",
  tableBorder:  "#D0D5DD",
  tableHeaderBg:"#0F2B46",
  tableHeaderFg:"#FFFFFF",
  rowAlt:       "#F8F9FA",
  text:         "#1A1A2E",
  muted:        "#5F6B7A",
  success:      "#2E7D32",
  warning:      "#E65100",
  divider:      "#E0E0E0",
};

/* ── page metrics ───────────────────────────────────────────────── */
const PAGE = {
  marginLeft:   52,
  marginRight:  52,
  marginTop:    60,
  marginBottom: 70,
  width:        612,   // US Letter
  height:       792,
};
const CONTENT_W = PAGE.width - PAGE.marginLeft - PAGE.marginRight;

/* -- helper: sanitise text for PDFKit built-in fonts ------------- */
/* Built-in fonts only support WinAnsiEncoding.  Strip or replace   */
/* any characters outside that range so the PDF never shows garbled */
/* glyphs.                                                          */
function sanitize(text) {
  return String(text)
    .replace(/[\u2022\u2023\u25CF\u25CB\u2219]/g, "-")   // bullets
    .replace(/[\u00D7\u2715\u2716\u2717]/g, "x")          // multiplication / crosses
    .replace(/[\u2610\u2611\u2612\u2713\u2714]/g, "[ ]")   // checkboxes / checkmarks
    .replace(/[\u2500-\u257F]/g, "-")                      // box-drawing
    .replace(/[\u2550-\u256C]/g, "=")                      // double box-drawing
    .replace(/[\u2190-\u21FF]/g, "->")                     // arrows
    .replace(/[\u2018\u2019]/g, "'")                       // smart single quotes
    .replace(/[\u201C\u201D]/g, '"')                       // smart double quotes
    .replace(/[\u2013\u2014]/g, "-")                       // en-dash, em-dash
    .replace(/[\u2026]/g, "...")                           // ellipsis
    .replace(/[\u00B1]/g, "+/-")                           // plus-minus
    .replace(/[^\x20-\x7E\xA0-\xFF\n\r\t]/g, "");         // drop anything else outside WinAnsi
}

/* -- helper: safe string with confidence marker ------------------- */
function s(v) { return sanitize(String(v ?? "-")); }
function sConf(value, confidence) {
  const base = sanitize(String(value ?? "-"));
  if (!confidence) return base;
  const c = String(confidence).toLowerCase();
  if (c.includes("partial") || c.includes("infer")) return "! " + base;
  return base;
}

/* -- helper: measure multi-line height of a cell ---------------- */
function cellHeight(doc, text, width, fontSize) {
  doc.fontSize(fontSize);
  return doc.heightOfString(sanitize(String(text ?? "")), { width, lineGap: 2 });
}

/* -- draw page footer ------------------------------------------- */
function footer(doc, pageNum) {
  const y = PAGE.height - PAGE.marginBottom + 18;

  /* Draw the divider line */
  doc.save();
  doc.strokeColor(C.divider).lineWidth(0.5)
     .moveTo(PAGE.marginLeft, y - 6)
     .lineTo(PAGE.width - PAGE.marginRight, y - 6)
     .stroke();
  doc.restore();

  /* Suppress auto-pagination while drawing below the normal content area */
  const savedBottom = doc.page.margins.bottom;
  const savedTop    = doc.page.margins.top;
  doc.page.margins.bottom = 0;
  doc.page.margins.top    = 0;

  doc.fontSize(7.5).fillColor(C.muted);
  doc.text("Generated by ED Backend Checker  |  AI + OCR Engineering Drawing Analysis",
           PAGE.marginLeft, y, { width: CONTENT_W * 0.7, align: "left", lineBreak: false });
  doc.text("Page " + pageNum,
           PAGE.marginLeft, y, { width: CONTENT_W, align: "right", lineBreak: false });

  doc.page.margins.bottom = savedBottom;
  doc.page.margins.top    = savedTop;
}

/* ── add new page with footer ───────────────────────────────────── */
let _pageCount = 0;
function newPage(doc) {
  if (_pageCount > 0) doc.addPage();
  _pageCount++;
  footer(doc, _pageCount);
  return PAGE.marginTop;
}

/* ── ensure enough room; if not, new page ───────────────────────── */
function ensureSpace(doc, needed) {
  if (doc.y + needed > PAGE.height - PAGE.marginBottom - 10) {
    return newPage(doc);
  }
  return doc.y;
}

/* -- section heading (blue bar) --------------------------------- */
function sectionHeading(doc, label) {
  doc.y = ensureSpace(doc, 32);
  const y = doc.y;
  doc.save();
  doc.roundedRect(PAGE.marginLeft, y, CONTENT_W, 24, 4)
     .fill(C.accent);
  doc.fontSize(11).fillColor("#FFFFFF")
     .text(sanitize(label).toUpperCase(), PAGE.marginLeft + 10, y + 6,
           { width: CONTENT_W - 20 });
  doc.restore();
  doc.y = y + 32;
}

/* -- sub-section heading (lighter) ------------------------------ */
function subHeading(doc, label) {
  doc.y = ensureSpace(doc, 26);
  const y = doc.y;
  doc.save();
  doc.roundedRect(PAGE.marginLeft, y, CONTENT_W, 20, 3)
     .fill(C.accentLight);
  doc.fontSize(9.5).fillColor(C.accent)
     .text(sanitize(label), PAGE.marginLeft + 10, y + 5,
           { width: CONTENT_W - 20 });
  doc.restore();
  doc.y = y + 26;
}

/* -- key-value pair --------------------------------------------- */
function kvLine(doc, key, value) {
  doc.y = ensureSpace(doc, 16);
  const y = doc.y;
  doc.fontSize(9).fillColor(C.muted)
     .text(sanitize(key) + ":", PAGE.marginLeft + 4, y, { continued: true, width: 130 });
  doc.fillColor(C.text)
     .text("  " + s(value), { width: CONTENT_W - 140 });
  doc.y = Math.max(doc.y, y + 14);
}

/* -- paragraph -------------------------------------------------- */
function paragraph(doc, text, opts = {}) {
  const fontSize = opts.fontSize || 9;
  const color = opts.color || C.text;
  const maxW = opts.width || CONTENT_W - 8;
  
  // Only ensure enough space to start the paragraph. Let PDFKit's
  // built-in text engine handle automatic page breaking for the rest.
  doc.y = ensureSpace(doc, 24);
  
  doc.fontSize(fontSize).fillColor(color)
     .text(s(text), PAGE.marginLeft + 4, doc.y, { width: maxW, lineGap: 2 });
  doc.y += 6;
}

/* ═══════════════════════════════════════════════════════════════════
   TABLE RENDERER
   – proper margins, word-wrap, alternating rows, header row
   ═══════════════════════════════════════════════════════════════════ */

/**
 * Draw a table.
 * @param {PDFDocument} doc
 * @param {string[]} headers
 * @param {string[][]} rows
 * @param {number[]} colWidths - proportional weights (they'll be normalised)
 */
function drawTable(doc, headers, rows, colWidths) {
  if (!headers.length) return;

  const PAD = 6;
  const FONT_H = 8;
  const FONT_B = 8.5;

  /* Normalise column widths to fit inside CONTENT_W */
  const totalWeight = colWidths.reduce((a, b) => a + b, 0);
  const widths = colWidths.map(w => Math.floor((w / totalWeight) * CONTENT_W));
  /* Fix rounding: give leftover px to last column */
  const used = widths.reduce((a, b) => a + b, 0);
  widths[widths.length - 1] += (CONTENT_W - used);

  /* ---- helper: compute row height (tallest cell) ---- */
  function rowHeight(cells, fontSize) {
    let max = 0;
    for (let c = 0; c < cells.length; c++) {
      const cw = widths[c] - PAD * 2;
      const h = cellHeight(doc, cells[c], cw > 10 ? cw : 10, fontSize);
      if (h > max) max = h;
    }
    // Cap height to max usable page space so it doesn't break pagination logic
    const MAX_H = PAGE.height - PAGE.marginTop - PAGE.marginBottom - 20;
    return Math.min(max + PAD * 2, MAX_H);
  }

  /* ---- draw a single row ---- */
  function renderRow(cells, y, bg, fg, fontSize, isBold) {
    const rh = rowHeight(cells, fontSize);

    /* background */
    doc.save();
    doc.rect(PAGE.marginLeft, y, CONTENT_W, rh).fill(bg);
    doc.restore();

    /* Suppress PDFKit auto-pagination while we draw at explicit coordinates */
    const savedBottom = doc.page.margins.bottom;
    const savedTop    = doc.page.margins.top;
    doc.page.margins.bottom = 0;
    doc.page.margins.top    = 0;

    /* cell text */
    let x = PAGE.marginLeft;
    for (let c = 0; c < cells.length; c++) {
      const cw = widths[c];

      /* vertical grid line */
      if (c > 0) {
        doc.save();
        doc.strokeColor(C.tableBorder).lineWidth(0.3)
           .moveTo(x, y).lineTo(x, y + rh).stroke();
        doc.restore();
      }

      doc.fontSize(fontSize).fillColor(fg);
      doc.text(s(cells[c]), x + PAD, y + PAD, {
        width: cw - PAD * 2,
        height: rh - PAD * 2,
        ellipsis: true,
        lineGap: 2,
        lineBreak: true,
      });

      x += cw;
    }

    doc.page.margins.bottom = savedBottom;
    doc.page.margins.top    = savedTop;

    /* bottom border */
    doc.save();
    doc.strokeColor(C.tableBorder).lineWidth(0.3)
       .moveTo(PAGE.marginLeft, y + rh)
       .lineTo(PAGE.marginLeft + CONTENT_W, y + rh)
       .stroke();
    doc.restore();

    return rh;
  }

  /* ---- header ---- */
  let y = ensureSpace(doc, rowHeight(headers, FONT_B) + 6);
  const headerH = renderRow(headers, y, C.tableHeaderBg, C.tableHeaderFg, FONT_B, true);
  y += headerH;

  /* ---- data rows ---- */
  for (let r = 0; r < rows.length; r++) {
    const cells = rows[r];
    const rh = rowHeight(cells, FONT_H);
    y = ensureSpace(doc, rh);
    const bg = r % 2 === 0 ? C.bg : C.rowAlt;
    const drawnH = renderRow(cells, y, bg, C.text, FONT_H, false);
    y += drawnH;
  }

  /* outer border */
  doc.save();
  doc.strokeColor(C.tableBorder).lineWidth(0.5)
     .rect(PAGE.marginLeft, doc.y - 0.5, CONTENT_W, 0).stroke();
  doc.restore();

  doc.y = y + 8;
}

/* ═══════════════════════════════════════════════════════════════════
   COVER HEADER
   ═══════════════════════════════════════════════════════════════════ */
function coverHeader(doc, title, subtitle) {
  const barH = 72;
  doc.save();
  doc.rect(0, 0, PAGE.width, barH).fill(C.headerBg);
  /* accent stripe */
  doc.rect(0, barH, PAGE.width, 4).fill(C.accent);
  doc.fontSize(20).fillColor(C.headerFg)
     .text(sanitize(title || "Engineering Drawing Report"), PAGE.marginLeft, 16,
           { width: CONTENT_W });
  doc.fontSize(9).fillColor("#A0B4CC")
     .text(sanitize(subtitle || "Standardized ED Analysis - AI + OCR"),
           PAGE.marginLeft, 44, { width: CONTENT_W });
  doc.restore();
  doc.y = barH + 20;
}

/* ═══════════════════════════════════════════════════════════════════
   STATUS BADGE
   ═══════════════════════════════════════════════════════════════════ */
function statusBadge(doc, status) {
  const label = s(status).toUpperCase();
  const color = /extract/i.test(label) ? C.success
              : /infer/i.test(label)   ? "#F9A825"
              : /pending/i.test(label) ? C.warning
              : C.muted;
  return label;
}

/* ═══════════════════════════════════════════════════════════════════
   BUILD A REPORT FOR ONE DOCUMENT
   Only includes information relevant to manufacturing the part:
   drawing identity, material, dimensions, tolerances, subparts, notes.
   AI/OCR meta-sections are intentionally excluded.
   ═══════════════════════════════════════════════════════════════════ */
function renderSingleReport(doc, report, index, total) {
  const ed = report?.standardED || report || {};

  /* ── cover / document separator ─────────────────── */
  if (index === 0) {
    coverHeader(doc,
      ed.title || "Engineering Drawing Report",
      "Document " + (index + 1) + (total > 1 ? " of " + total : "") +
      "  |  " + (report?.fileName || ed.source?.filename || "uploaded file") +
      "  |  ID: " + (ed.standardEdId || "N/A")
    );
  } else {
    doc.y = newPage(doc);
    coverHeader(doc,
      "Document " + (index + 1) + " of " + total,
      (ed.title || "Engineering Drawing") + "  |  " + (report?.fileName || ed.source?.filename || "uploaded file")
    );
  }

  /* ── confidence legend ───────────────────────────── */
  doc.y = ensureSpace(doc, 14);
  doc.fontSize(7.5).fillColor(C.muted)
     .text("! = partially visible or inferred value — verify manually against original drawing",
           PAGE.marginLeft + 4, doc.y, { width: CONTENT_W - 8 });
  doc.y += 10;

  /* ── drawing identity (title block — no Scale row) ── */
  if (ed.titleBlock && Object.values(ed.titleBlock).some(v => v)) {
    sectionHeading(doc, "Drawing Identity");

    /* Build rows, skipping Scale (do not scale drawing) and any null/empty values */
    const identityRows = [
      ["Drawing Number", s(ed.titleBlock.drawingNumber)],
      ["Revision",       s(ed.titleBlock.revision)],
      ["Units",          s(ed.titleBlock.units)],
      ["Sheet",          s(ed.titleBlock.sheet)],
      ["Customer",       s(ed.titleBlock.customer)],
      ["Material Std.",  s(ed.titleBlock.materialStandard)],
    ].filter(([, v]) => v && v !== "-");

    if (identityRows.length) {
      drawTable(doc, ["Field", "Value"], identityRows, [3, 7]);
    }
  }

  /* ── part / assembly info ───────────────────────── */
  const assemblyDesc = ed.assembly?.description || ed.part?.function || null;
  const assemblyMat  = ed.assembly?.material || ed.part?.material || null;
  const assemblyFin  = ed.assembly?.finish || null;
  const partRows = [
    ["Part / Assembly Name", s(ed.title)],
    ["Description",          s(assemblyDesc)],
    ["Material",             s(assemblyMat)],
    ["Finish",               s(assemblyFin)],
    ["General Tolerances",   s(ed.titleBlock?.generalTolerances)],
    ["Default Surface Finish",s(ed.titleBlock?.surfaceFinish)],
    ["Projection",           s(ed.titleBlock?.projection)],
  ].filter(([, v]) => v && v !== "-");

  if (partRows.length) {
    sectionHeading(doc, "Part / Assembly Information");
    drawTable(doc, ["Property", "Value"], partRows, [3, 7]);
  }

  /* ── assembled product overview ─────────────────── */
  const ap = ed.assembledProduct;
  if (ap) {
    sectionHeading(doc, "Assembled Product");

    if (ap.description) {
      paragraph(doc, ap.description);
    }

    // Overall envelope dimensions
    const overallDims = Array.isArray(ap.overallDimensions) ? ap.overallDimensions.filter(d => d.value) : [];
    if (overallDims.length) {
      subHeading(doc, "Overall / Envelope Dimensions");
      drawTable(doc,
        ["Dimension", "Value", "Unit", "Tolerance", "Note"],
        overallDims.map(d => [s(d.name), sConf(d.value, d.confidence), s(d.unit), sConf(d.tolerance, d.confidence), s(d.note)]),
        [3, 2, 1, 2, 5]
      );
    }

    // Key specs
    const keySpecs = [
      ["Bore / Inner Cavity", s(ap.bore)],
      ["Hollowness / Internal Geometry", s(ap.hollowness)],
      ["Total Weight", s(ap.weight)],
      ["Grease Specification", s(ap.grease)],
    ].filter(([, v]) => v && v !== "-");
    if (keySpecs.length) {
      drawTable(doc, ["Property", "Value"], keySpecs, [3, 7]);
    }

    // Specification of Size table
    const specSize = Array.isArray(ap.specificationOfSize) ? ap.specificationOfSize.filter(r => r.value) : [];
    if (specSize.length) {
      subHeading(doc, "Specification of Size");
      drawTable(doc,
        ["Parameter", "Value", "Unit"],
        specSize.map(r => [s(r.name), s(r.value), s(r.unit)]),
        [4, 4, 2]
      );
    }

    // Hardness table
    const hardness = Array.isArray(ap.hardnessTable) ? ap.hardnessTable.filter(r => r.part) : [];
    if (hardness.length) {
      subHeading(doc, "Heat Treatment / Hardness");
      drawTable(doc,
        ["Part", "Surface HRC", "Core HRC", "Case Depth (mm)"],
        hardness.map(r => [s(r.part), s(r.surfaceHRC), s(r.coreHRC), s(r.caseDepthMm)]),
        [3, 2.5, 2.5, 3]
      );
    }

    // Unspecified tolerances
    const unspecTol = Array.isArray(ap.unspecifiedTolerances) ? ap.unspecifiedTolerances.filter(r => r.range) : [];
    if (unspecTol.length) {
      subHeading(doc, "Unspecified Tolerances");
      drawTable(doc,
        ["Dimension Range", "Tolerance"],
        unspecTol.map(r => [s(r.range), s(r.tolerance)]),
        [5, 5]
      );
    }
  }

  /* ── parts list / BOM ────────────────────────────── */
  const partslist = Array.isArray(ed.partslist) ? ed.partslist.filter(p => p && (p.description || p.partNumber)) : [];
  if (partslist.length) {
    sectionHeading(doc, "Parts List / BOM  (" + partslist.length + " items)");
    drawTable(doc,
      ["Item No.", "Part Number", "Description", "Qty", "Material", "Notes"],
      partslist.map(p => [s(p.itemNo), s(p.partNumber), s(p.description), s(p.quantity), s(p.material), s(p.notes)]),
      [1.2, 2, 3.5, 1, 3, 3.3]
    );
  }

  /* ── summary ─────────────────────────────────────── */
  if (ed.summary) {
    doc.y = ensureSpace(doc, 20);
    doc.fontSize(8.5).fillColor(C.muted)
       .text(s(ed.summary), PAGE.marginLeft + 4, doc.y, { width: CONTENT_W - 8, lineGap: 2 });
    doc.y += 8;
  }

  /* ════════════════════════════════════════════════════
     SUBPARTS — each with its own dimension table
     Shows: name, qty, function, dimensions (value/unit/tolerance/note)
     ════════════════════════════════════════════════════ */
  const subparts = Array.isArray(ed.subparts) ? ed.subparts : [];
  if (subparts.length) {
    sectionHeading(doc, "Subparts  (" + subparts.length + ")");
    for (let si = 0; si < subparts.length; si++) {
      const sp = subparts[si];
      subHeading(doc, (si + 1) + ".  " + s(sp.name) + "   qty: " + (sp.quantity || 1));

      if (sp.function) kvLine(doc, "Function", sp.function);

      /* Manufacturing notes — skip generic AI disclaimer lines */
      const DISCLAIMER_PATTERNS = [
        /heuristic/i, /confirm.*engineer/i, /should be (confirmed|verified)/i,
        /interpretation is/i, /document control/i,
      ];
      const notes = (Array.isArray(sp.notes) ? sp.notes : [])
        .filter(n => n && !DISCLAIMER_PATTERNS.some(p => p.test(n)));
      if (notes.length) {
        doc.y = ensureSpace(doc, 14);
        doc.fontSize(8.5).fillColor(C.muted).text("Notes:", PAGE.marginLeft + 4, doc.y);
        for (const n of notes) {
          doc.y = ensureSpace(doc, 12);
          doc.fontSize(8).fillColor(C.text)
             .text("  -  " + s(n), PAGE.marginLeft + 12, doc.y, { width: CONTENT_W - 20 });
        }
        doc.y += 4;
      }

      /* Subpart dimensions — drop Status column, keep what matters */
      const spDims = (Array.isArray(sp.dimensions) ? sp.dimensions : [])
        .filter(d => d.value && d.value !== "-");
      if (spDims.length) {
        doc.y = ensureSpace(doc, 26);
        doc.fontSize(8.5).fillColor(C.accent)
           .text("Dimensions — " + s(sp.name), PAGE.marginLeft + 4, doc.y);
        doc.y += 4;
        drawTable(doc,
          ["Dimension", "Value", "Unit", "Tolerance", "Note"],
          spDims.map(d => [
            s(d.name),
            sConf(d.value, d.confidence),
            s(d.unit),
            sConf(d.tolerance, d.confidence),
            s(d.note || d.explanation),
          ]),
          [3, 2, 1, 2, 6]
        );
      }

      /* Geometric tolerances */
      const gdt = (Array.isArray(sp.geometricTolerances) ? sp.geometricTolerances : []).filter(g => g.value);
      if (gdt.length) {
        doc.y = ensureSpace(doc, 26);
        doc.fontSize(8.5).fillColor(C.accent)
           .text("GD&T — " + s(sp.name), PAGE.marginLeft + 4, doc.y);
        doc.y += 4;
        drawTable(doc,
          ["Symbol", "Value", "Datum", "Note"],
          gdt.map(g => [s(g.symbol), s(g.value), s(g.datum), s(g.note)]),
          [3, 2, 1.5, 6]
        );
      }

      /* Manufacturing notes */
      const mfgNotes = (Array.isArray(sp.manufacturingNotes) ? sp.manufacturingNotes : []).filter(Boolean);
      if (mfgNotes.length) {
        doc.y = ensureSpace(doc, 14);
        subHeading(doc, "Manufacturing / Heat Treatment Notes — " + s(sp.name));
        for (const n of mfgNotes) {
          doc.y = ensureSpace(doc, 12);
          doc.fontSize(8).fillColor(C.text)
             .text("  -  " + s(n), PAGE.marginLeft + 12, doc.y, { width: CONTENT_W - 20 });
        }
        doc.y += 4;
      }

      /* Divider between subparts */
      if (si < subparts.length - 1) {
        doc.y = ensureSpace(doc, 10);
        doc.save();
        doc.strokeColor(C.divider).lineWidth(0.4)
           .moveTo(PAGE.marginLeft + 20, doc.y)
           .lineTo(PAGE.width - PAGE.marginRight - 20, doc.y)
           .stroke();
        doc.restore();
        doc.y += 8;
      }
    }
  }

  /* ════════════════════════════════════════════════════
     ASSEMBLY DIMENSIONS TABLE
     Shows: name, value, unit, tolerance, note
     ════════════════════════════════════════════════════ */
  const dims = (Array.isArray(ed.assemblyDimensions) ? ed.assemblyDimensions :
                Array.isArray(ed.dimensions) ? ed.dimensions : [])
    .filter(d => d.value && d.value !== "-");
  if (dims.length) {
    sectionHeading(doc, "Assembly Dimensions  (" + dims.length + ")");
    drawTable(doc,
      ["Dimension", "Value", "Unit", "Tolerance", "Note"],
      dims.map(d => [
        s(d.name), sConf(d.value, d.confidence), s(d.unit), sConf(d.tolerance, d.confidence), s(d.note || d.explanation),
      ]),
      [3, 2, 1, 2, 6]
    );
  }

  /* ── general notes ────────────────────────────────── */
  const generalNotes = (Array.isArray(ed.generalNotes) ? ed.generalNotes : []).filter(Boolean);
  if (generalNotes.length) {
    sectionHeading(doc, "General Notes  (" + generalNotes.length + ")");
    for (let ni = 0; ni < generalNotes.length; ni++) {
      doc.y = ensureSpace(doc, 14);
      doc.fontSize(8.5).fillColor(C.text)
         .text((ni + 1) + ".  " + s(generalNotes[ni]), PAGE.marginLeft + 8, doc.y, { width: CONTENT_W - 16 });
    }
    doc.y += 6;
  }

  /* ── manufacturing checks (from review.actions only) ── */
  const actions = (ed.review?.actions || []).filter(Boolean);
  if (actions.length) {
    sectionHeading(doc, "Manufacturing Checks");
    for (const a of actions) {
      doc.y = ensureSpace(doc, 14);
      doc.fontSize(8.5).fillColor(C.text)
         .text("  [ ]  " + s(a), PAGE.marginLeft + 8, doc.y, { width: CONTENT_W - 16 });
    }
    doc.y += 6;
  }
}

/* ═══════════════════════════════════════════════════════════════════
   FEATURE CATEGORIZATION SECTION
   ═══════════════════════════════════════════════════════════════════ */
function renderCategorization(doc, categorization) {
  if (!categorization) return;

  sectionHeading(doc, "Feature Categorization & Manufacturing Routing");

  // Part family badge
  doc.y = ensureSpace(doc, 44);
  const pfY = doc.y;
  doc.save();
  doc.roundedRect(PAGE.marginLeft, pfY, CONTENT_W, 22, 4).fill("#0F2B46");
  doc.fontSize(11).fillColor("#FFFFFF")
     .text(
       "PART FAMILY:  " + sanitize(categorization.partFamily || "UNKNOWN") +
       "   (" + sanitize(categorization.partFamilyConfidence || "") + " confidence)",
       PAGE.marginLeft + 10, pfY + 5, { width: CONTENT_W - 20 }
     );
  doc.restore();
  doc.y = pfY + 28;

  // Show top 3 scoring families
  if (categorization.partFamilyScores) {
    const top3 = Object.entries(categorization.partFamilyScores)
      .sort((a, b) => b[1] - a[1])
      .slice(0, 3)
      .filter(([, score]) => score > 0);
    if (top3.length) {
      doc.y = ensureSpace(doc, 14);
      doc.fontSize(8).fillColor(C.muted)
         .text(
           "Classification scores: " + top3.map(([fam, sc]) => `${fam}=${sc}`).join("  |  "),
           PAGE.marginLeft + 4, doc.y, { width: CONTENT_W - 8 }
         );
      doc.y += 10;
    }
  }

  // Feature category breakdown
  if (Array.isArray(categorization.features) && categorization.features.length) {
    subHeading(doc, "Extracted Features by Category");

    // Group by category
    const grouped = {};
    for (const f of categorization.features) {
      const cat = f.category || "Other";
      if (!grouped[cat]) grouped[cat] = [];
      grouped[cat].push(f);
    }

    const rows = [];
    for (const [cat, feats] of Object.entries(grouped)) {
      for (const f of feats) {
        rows.push([
          s(f.sourcePart),
          s(cat),
          s(f.subCategory),
          s(f.name),
          s(f.value),
          s(f.unit),
          s(f.tolerance),
        ]);
      }
    }

    drawTable(doc,
      ["Part", "Category", "Sub-Category", "Feature", "Value", "Unit", "Tolerance"],
      rows,
      [2.5, 2.5, 2.5, 3, 2, 1, 1.5]
    );
  }

  // Manufacturing categories
  if (Array.isArray(categorization.manufacturingCategories) && categorization.manufacturingCategories.length) {
    subHeading(doc, "Manufacturing Categories Required");
    const mcRows = categorization.manufacturingCategories.map((cat, i) => [
      String(i + 1),
      s(cat),
      s(categorization.processes?.[i]),
    ]);
    drawTable(doc, ["#", "Manufacturing Category", "Process"], mcRows, [0.5, 4, 5.5]);
  }

  // Routing suggestion
  if (Array.isArray(categorization.routingSuggestion) && categorization.routingSuggestion.length) {
    subHeading(doc, "AI Routing Suggestion");
    const routeRows = categorization.routingSuggestion.map(r => [
      String(r.seq),
      s(r.operation),
      s(r.process),
      s(r.machine || "TBD"),
    ]);
    drawTable(doc, ["Seq", "Operation", "Process", "Machine"], routeRows, [0.8, 3, 4.5, 2.7]);

    doc.y = ensureSpace(doc, 16);
    doc.fontSize(7.5).fillColor(C.muted)
       .text("* Routing is AI-suggested. Review and approve before releasing to shop floor.",
             PAGE.marginLeft + 4, doc.y, { width: CONTENT_W - 8 });
    doc.y += 10;
  }
}
function renderComparison(doc, comparison) {
  if (!comparison) return;

  doc.y = newPage(doc);
  coverHeader(doc, "Document Comparison", (comparison.documents?.[0]?.fileName || "Doc 1") + "  vs  " + (comparison.documents?.[1]?.fileName || "Doc 2"));

  sectionHeading(doc, "Summary");
  paragraph(doc, comparison.summary || "No comparison summary available.");

  if (comparison.dimensions?.rows?.length) {
    sectionHeading(doc, "Dimension Comparison");
    drawTable(doc,
      comparison.dimensions.headers || ["Dimension", "Doc 1", "Doc 2", "Status"],
      comparison.dimensions.rows.map(r => r.map(s)),
      [4, 3, 3, 2]
    );
  }

  if (comparison.subparts?.rows?.length) {
    sectionHeading(doc, "Subpart Comparison");
    drawTable(doc,
      comparison.subparts.headers || ["Subpart", "Doc 1", "Doc 2", "Status"],
      comparison.subparts.rows.map(r => r.map(s)),
      [4, 2, 2, 2]
    );
  }

  if (comparison.uniqueToDoc2?.dimensions?.length || comparison.uniqueToDoc2?.subparts?.length) {
    sectionHeading(doc, "Unique to Document 2");
    if (comparison.uniqueToDoc2.dimensions?.length) {
      kvLine(doc, "Dimensions", comparison.uniqueToDoc2.dimensions.join(", "));
    }
    if (comparison.uniqueToDoc2.subparts?.length) {
      kvLine(doc, "Subparts", comparison.uniqueToDoc2.subparts.join(", "));
    }
  }
}

