/**
 * Build a professional PDF buffer from one or more analysed documents.
 *
 * @param {object|object[]} standardEdDocuments – single report or array
 * @param {string}          sourcePath          – route label
 * @param {object|null}     comparison          – multi-doc comparison
 * @returns {Promise<Buffer>}
 */
function buildProfessionalPdf(standardEdDocuments, sourcePath, comparison) {
  return new Promise((resolve, reject) => {
    _pageCount = 0;

    const doc = new PDFDocument({
      size: "LETTER",
      margins: {
        top:    PAGE.marginTop,
        bottom: PAGE.marginBottom,
        left:   PAGE.marginLeft,
        right:  PAGE.marginRight,
      },
      bufferPages: true,
      info: {
        Title:    "Engineering Drawing Report",
        Author:   "ED Backend Checker — AI + OCR",
        Subject:  "Standardized Engineering Drawing Analysis",
        Keywords: "engineering drawing, OCR, dimensions, subparts",
      },
    });

    const chunks = [];
    doc.on("data", (chunk) => chunks.push(chunk));
    doc.on("end", () => resolve(Buffer.concat(chunks)));
    doc.on("error", reject);

    try {
      const reports = Array.isArray(standardEdDocuments)
        ? standardEdDocuments
        : [standardEdDocuments];

      /* first page footer */
      _pageCount = 1;
      footer(doc, 1);

      for (let i = 0; i < reports.length; i++) {
        if (i > 0) {
          /* new page for subsequent docs already handled in renderSingleReport */
        }
        renderSingleReport(doc, reports[i], i, reports.length);

        // Render feature categorization if available
        if (reports[i]._categorization) {
          doc.y = ensureSpace(doc, 40);
          renderCategorization(doc, reports[i]._categorization);
        }
      }

      if (comparison) {
        renderComparison(doc, comparison);
      }

      /* ── update footers on all pages (PDFKit buffered mode) ── */
      const totalPages = doc.bufferedPageRange().count;
      for (let p = 0; p < totalPages; p++) {
        doc.switchToPage(p);
        /* clear old footer area and re-draw */
        const fy = PAGE.height - PAGE.marginBottom + 12;
        doc.save();
        doc.rect(PAGE.marginLeft - 2, fy, CONTENT_W + 4, PAGE.marginBottom).fill(C.bg);
        doc.restore();
        footer(doc, p + 1);
      }

      doc.end();
    } catch (err) {
      reject(err);
    }
  });
}

module.exports = { buildProfessionalPdf };
